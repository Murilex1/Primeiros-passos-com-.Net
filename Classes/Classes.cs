Classes e Objetos

Classes são os tipos mais fundamentais em C#.
Uma classe é uma estrutura de dados que combina estado (campos) e  ações (métodos).

Objetos são instancias de uma classe.
As classes suportam herança e polimorfismo, mecanismos pelos quais as classes derivadas podem estender e especializar a classe base.


Instâncias de classes (objetos) são criadas usando o operador new, que aloca memória para umanova instância, chama um construtor para inicializar a instância e retorna uma referência a instância

Ponto p1 = new Ponto (0,0);
Ponto p2 = new Ponto(10,20);

